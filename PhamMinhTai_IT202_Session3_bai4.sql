-- =====================================================
-- PHÂN TÍCH BÀI TOÁN
-- =====================================================

-- Hệ thống có:
-- - hàng trăm ngàn đơn hàng bị hủy
-- - làm chậm truy vấn
-- - tốn dung lượng lưu trữ

-- Nhưng:
-- phòng Kế toán vẫn cần dữ liệu
-- để kiểm toán cuối năm


-- =====================================================
-- GIẢI PHÁP 1 - HARD DELETE
-- =====================================================

-- Xóa thật dữ liệu khỏi database

DELETE FROM Orders
WHERE Status = 'Canceled';


-- =====================================================
-- ƯU ĐIỂM
-- =====================================================

-- - Giải phóng dung lượng ổ cứng tốt
-- - Giảm số lượng bản ghi
-- - Query nhanh hơn


-- =====================================================
-- NHƯỢC ĐIỂM
-- =====================================================

-- - Mất dữ liệu vĩnh viễn
-- - Không thể kiểm toán lại
-- - Rủi ro cao nếu xóa nhầm


-- =====================================================
-- GIẢI PHÁP 2 - SOFT DELETE
-- =====================================================

-- Không xóa thật
-- chỉ đánh dấu đã xóa

UPDATE Orders
SET IsDeleted = 1
WHERE Status = 'Canceled';


-- =====================================================
-- ƯU ĐIỂM
-- =====================================================

-- - Giữ được lịch sử kế toán
-- - Có thể khôi phục dữ liệu
-- - An toàn hơn


-- =====================================================
-- NHƯỢC ĐIỂM
-- =====================================================

-- - Không giải phóng nhiều dung lượng
-- - Bảng vẫn lớn dần theo thời gian
-- - Query phải lọc thêm IsDeleted


-- =====================================================
-- SO SÁNH
-- =====================================================

-- Hard Delete
-- + Giải phóng dung lượng: Tốt
-- + Tốc độ query: Nhanh
-- - Lịch sử kế toán: Mất

-- Soft Delete
-- - Giải phóng dung lượng: Ít
-- + Lưu lịch sử kế toán
-- + An toàn dữ liệu


-- =====================================================
-- GIẢI PHÁP ĐƯỢC CHỌN
-- =====================================================

-- Chọn:
-- SOFT DELETE

-- Vì:
-- hệ thống siêu thị cần lưu lịch sử
-- để phục vụ kiểm toán


-- =====================================================
-- THÊM CỘT IsDeleted
-- =====================================================

ALTER TABLE Orders
ADD COLUMN IsDeleted TINYINT(1) DEFAULT 0;


-- =====================================================
-- ĐÁNH DẤU ĐƠN HÀNG HỦY
-- =====================================================

UPDATE Orders
SET IsDeleted = 1
WHERE Status = 'Canceled';


-- =====================================================
-- TRUY VẤN CHO ỨNG DỤNG BÁN HÀNG
-- =====================================================

-- Chỉ lấy đơn còn hoạt động

SELECT *
FROM Orders
WHERE IsDeleted = 0;


-- =====================================================
-- TRUY VẤN CHO KẾ TOÁN
-- =====================================================

-- Vẫn xem được đơn bị hủy

SELECT *
FROM Orders
WHERE Status = 'Canceled';


-- =====================================================
-- KẾT QUẢ
-- =====================================================

-- Đơn hàng bị hủy:
-- - "biến mất" khỏi hệ thống bán hàng
-- - nhưng vẫn tồn tại để kiểm toán